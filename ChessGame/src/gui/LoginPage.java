    public boolean loginTest(String user, String password){
                        
        try(WebControl web = new WebControl()){
            
            web.sendMsgWithFlag("database");
            web.sendMsgWithFlag("login");
            web.sendMsgWithFlag(user);
            web.sendMsgWithFlag(password);
            
            String answ = web.readMsgWithFlag();
            if(answ.equals("ok")){
                return true;
            }else{
                answ = web.readMsgWithFlag();
                Controler.errorMsg(answ);
                return false;
            }
            
        }catch(IOException e){
            System.err.println("Error in the loginTest");
            System.err.println(e.toString());
        }
        
        return false;
    }
    
